    private void AddUserButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddUserButtonActionPerformed
         String username = JOptionPane.showInputDialog(this, "Username:");
        if (username == null || username.trim().isEmpty()) return;

        String password = JOptionPane.showInputDialog(this, "Password:");
        if (password == null || password.trim().isEmpty()) return;

        String email = JOptionPane.showInputDialog(this, "Email:");
        if (email == null || email.trim().isEmpty()) return;

        String[] types = {"Adult", "Child"};
        int choice = JOptionPane.showOptionDialog(this, "User Type:", "Select Type",
            JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE,
            null, types, types[0]);
        if (choice == -1) return;
        int userType = (choice == 0) ? 1 : 2;

        try (Connection conn = DatabaseConnection.connect()) {
            String sql = "INSERT INTO Users (UserId, Username, Password, UserType, Email) VALUES (?, ?, ?, ?, ?)";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setNull(1, java.sql.Types.INTEGER);
            ps.setString(2, username.trim());
            ps.setString(3, password.trim());
            ps.setInt(4, userType);
            ps.setString(5, email.trim());
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "User created!");
            loadUsers();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_AddUserButtonActionPerformed

    private void DeleteUserButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteUserButtonActionPerformed
         int row = jTable1.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select a user first!");
            return;
        }

        int userId = (int) jTable1.getValueAt(row, 0);
        String username = (String) jTable1.getValueAt(row, 1);

        int confirm = JOptionPane.showConfirmDialog(this,
            "Delete user '" + username + "'?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) return;

        try (Connection conn = DatabaseConnection.connect()) {
            PreparedStatement ps = conn.prepareStatement("DELETE FROM Users WHERE UserId = ?");
            ps.setInt(1, userId);
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "User deleted!");
            loadUsers();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_DeleteUserButtonActionPerformed

    private void ResetPasswordButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ResetPasswordButtonActionPerformed
         int row = jTable1.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select a user first!");
            return;
        }

        int userId = (int) jTable1.getValueAt(row, 0);
        String username = (String) jTable1.getValueAt(row, 1);

        String newPass = JOptionPane.showInputDialog(this, "New password for '" + username + "':");
        if (newPass == null || newPass.trim().isEmpty()) return;

        try (Connection conn = DatabaseConnection.connect()) {
            PreparedStatement ps = conn.prepareStatement("UPDATE Users SET Password = ? WHERE UserId = ?");
            ps.setString(1, newPass.trim());
            ps.setInt(2, userId);
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "Password reset!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_ResetPasswordButtonActionPerformed

