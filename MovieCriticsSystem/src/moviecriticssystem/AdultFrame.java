    private void ModerateContentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModerateContentButtonActionPerformed
         int row = jTable1.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Select a movie first!");
        return;
    }

    int movieId = (int) jTable1.getValueAt(row, 0);
    String movieTitle = (String) jTable1.getValueAt(row, 1);

    String[] options = {"Remove Comment", "Remove Rating", "Remove Both", "Cancel"};
    int choice = JOptionPane.showOptionDialog(this,
        "What do you want to moderate for: " + movieTitle,
        "Moderate Content",
        JOptionPane.DEFAULT_OPTION,
        JOptionPane.WARNING_MESSAGE,
        null, options, options[0]);

    if (choice == 3 || choice == -1) return;

    String sql;
    if (choice == 0) {
        sql = "UPDATE Movies SET Comments = NULL WHERE MovieID = ?";
    } else if (choice == 1) {
        sql = "UPDATE Movies SET Rating = 0 WHERE MovieID = ?";
    } else {
        sql = "UPDATE Movies SET Comments = NULL, Rating = 0 WHERE MovieID = ?";
    }

    try (Connection conn = DatabaseConnection.connect()) {
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setInt(1, movieId);
        ps.executeUpdate();

        JOptionPane.showMessageDialog(this, "Done!");
        loadMovies();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_ModerateContentButtonActionPerformed

    private void AnalyticsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AnalyticsButtonActionPerformed
       try (Connection conn = DatabaseConnection.connect()) {

        Statement stmt = conn.createStatement();


        ResultSet rs1 = stmt.executeQuery("SELECT COUNT(*) as total FROM Movies");
        int total = 0;
        if (rs1.next()) total = rs1.getInt("total");


        ResultSet rs2 = stmt.executeQuery("SELECT COUNT(*) as watched FROM Movies WHERE Watched = TRUE");
        int watched = 0;
        if (rs2.next()) watched = rs2.getInt("watched");


        ResultSet rs3 = stmt.executeQuery("SELECT AVG(Rating) as avg FROM Movies");
        double avg = 0;
        if (rs3.next()) avg = rs3.getDouble("avg");


        ResultSet rs4 = stmt.executeQuery("SELECT Title, Rating FROM Movies ORDER BY Rating DESC LIMIT 1");
        String topTitle = "-";
        int topRating = 0;
        if (rs4.next()) {
            topTitle = rs4.getString("Title");
            topRating = rs4.getInt("Rating");
        }


        String message =  "Total Movies     : " + total + "\n" + "Watched Movies   : " + watched + "\n" + "Unwatched Movies : " + (total - watched) + "\n" + "Average Rating   : " + String.format("%.1f", avg) + "\n" + "Top Rated Movie  : " + topTitle + " (" + topRating + "/10)";
         JOptionPane.showMessageDialog(this, message, "Analytics", JOptionPane.INFORMATION_MESSAGE);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
    }//GEN-LAST:event_AnalyticsButtonActionPerformed

    private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchButtonActionPerformed
      String keyword = SearchTextField.getText().trim();
    String genre = (String) GenreFilterCombo.getSelectedItem();

    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);

    String sql = "SELECT MovieID, Title, Genre, Language, ReleaseDate, Rating, Watched, ParentalRestriction, Comments FROM Movies WHERE 1=1";

    if (!keyword.isEmpty()) {
        sql += " AND (Title LIKE '%" + keyword + "%' OR Language LIKE '%" + keyword + "%')";
    }

    if (genre != null && !genre.equals("All")) {
        sql += " AND Genre = '" + genre + "'";
    }

    try (Connection conn = DatabaseConnection.connect();
         Statement stmt = conn.createStatement();
         ResultSet rs = stmt.executeQuery(sql)) {

        while (rs.next()) {
            Object[] row = {
                rs.getInt("MovieID"),
                rs.getString("Title"),
                rs.getString("Genre"),
                rs.getString("Language"),
                rs.getDate("ReleaseDate"),
                rs.getInt("Rating"),
                rs.getBoolean("Watched"),
                rs.getBoolean("ParentalRestriction"),
                rs.getString("Comments")
            };
            model.addRow(row);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_SearchButtonActionPerformed

